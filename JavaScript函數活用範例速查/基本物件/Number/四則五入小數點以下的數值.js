// 四則五入的方法
// ceil(null) 小數點以下無條件進位
// floor(null) 小數點以下無條件捨去
// round(null) 小數點以下四則五入

console.log(Math.ceil(4.56)); // 5
console.log(Math.ceil(-4.56)); // -4
console.log(Math.floor(4.56)); // 4
console.log(Math.floor(-4.56)); // -5
console.log(Math.round(4.56)); // 5
console.log(Math.round(-4.56)); // -5